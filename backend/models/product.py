class Product:
    def __init__(self, id, name, quantity, buy_price, sale_price, category_id, media_id, date):
        self.id = id
        self.name = name
        self.quantity = quantity
        self.buy_price = buy_price
        self.sale_price = sale_price
        self.category_id = category_id
        self.media_id = media_id
        self.date = date
